import { describe, it, expect } from "vitest";
import { aesCmac } from "../../src/scp03/cmac.js";

// RFC 4493 test vectors for AES-128-CMAC.
const KEY = Uint8Array.from([
  0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6, 0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf, 0x4f, 0x3c,
]);

const M = Uint8Array.from([
  0x6b, 0xc1, 0xbe, 0xe2, 0x2e, 0x40, 0x9f, 0x96, 0xe9, 0x3d, 0x7e, 0x11, 0x73, 0x93, 0x17, 0x2a,
  0xae, 0x2d, 0x8a, 0x57, 0x1e, 0x03, 0xac, 0x9c, 0x9e, 0xb7, 0x6f, 0xac, 0x45, 0xaf, 0x8e, 0x51,
  0x30, 0xc8, 0x1c, 0x46, 0xa3, 0x5c, 0xe4, 0x11, 0xe5, 0xfb, 0xc1, 0x19, 0x1a, 0x0a, 0x52, 0xef,
  0xf6, 0x9f, 0x24, 0x45, 0xdf, 0x4f, 0x9b, 0x17, 0xad, 0x2b, 0x41, 0x7b, 0xe6, 0x6c, 0x37, 0x10,
]);

function hex(u8: Uint8Array): string {
  return Buffer.from(u8).toString("hex");
}

describe("AES-128-CMAC (RFC 4493 vectors)", () => {
  it("empty message", () => {
    expect(hex(aesCmac(KEY, new Uint8Array(0)))).toBe("bb1d6929e95937287fa37d129b756746");
  });

  it("16-byte message (one full block)", () => {
    expect(hex(aesCmac(KEY, M.subarray(0, 16)))).toBe("070a16b46b4d4144f79bdd9dd04a287c");
  });

  it("40-byte message (non-multiple of block)", () => {
    expect(hex(aesCmac(KEY, M.subarray(0, 40)))).toBe("dfa66747de9ae63030ca32611497c827");
  });

  it("64-byte message (exact multiple, multiple blocks)", () => {
    expect(hex(aesCmac(KEY, M))).toBe("51f0bebf7e3b9d92fc49741779363cfe");
  });
});
